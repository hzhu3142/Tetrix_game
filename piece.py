# index 0 - 6 represent shape


class Piece(object):  # *
    def __init__(self, x=5, y=0):
        self.x = x
        self.y = y
        indx = random.randint(0, 6)
        self.shape = shapes[indx]
        self.color = shape_colors[indx]
        self.rotation = random.randint(0,len(self.shape)-1)

    # this function is originally called: convert_shape_format
    def update_position(self):
        self.rotation = self.rotation % len(self.shape)
        positions = []
        format = self.shape[self.rotation]
        m, n = len(format), len(format[0])
        for i in range(m):
            for j in range(n):
                if format[i][j] == '0':
                    positions.append((self.x + j - 2, self.y + i - 4))

        return positions
